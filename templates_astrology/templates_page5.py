def get_page5_blocks(pattern: str) -> dict[str, dict[str, str]]:
    """
    Page5 用ブロック一式を取得する関数。

    pattern:
        "A" / "B" / "C" / "D" を想定。
        それ以外が来た場合は A を返す。

    戻り値:
        {
          "good":   {"body": str, "summary": str},
          "gap":    {"body": str, "summary": str},
          "growth": {"body": str, "summary": str},
        }
    """
    if not pattern:
        key = "A"
    else:
        key = pattern.strip().upper()
